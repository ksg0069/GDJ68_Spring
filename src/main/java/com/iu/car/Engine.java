package com.iu.car;

public class Engine {
	
	private String fuel;
	private int hp;
	private int gitong;
	
	
	
	public String getFuel() {
		return fuel;
	}
	public void setFuel(String fuel) {
		this.fuel = fuel;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getGitong() {
		return gitong;
	}
	public void setGitong(int gitong) {
		this.gitong = gitong;
	}
	
	
	

}
